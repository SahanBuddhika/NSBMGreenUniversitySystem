
package nsbmcourseenrollmentsystem;


public class PostergraduateStudentDetails {
    private String studentid;
    private String firstname;
    private String lastname;
    private int age;
    private String gender;
    private String emailaddress;
    private String address;
    private String phonenumber;
    private int yearofregistration;
    private String qualificationtypes;
    private String institution;
    private int yearofcompletion;
    

    /**
     * @return the studentid
     */
    public String getStudentid() {
        return studentid;
    }

    /**
     * @param studentid the studentid to set
     */
    public void setStudentid(String studentid) {
        this.studentid = studentid;
    }

    /**
     * @return the firstname
     */
    public String getFirstname() {
        return firstname;
    }

    /**
     * @param firstname the firstname to set
     */
    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    /**
     * @return the lastname
     */
    public String getLastname() {
        return lastname;
    }

    /**
     * @param lastname the lastname to set
     */
    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    /**
     * @return the age
     */
    public int getAge() {
        return age;
    }

    /**
     * @param age the age to set
     */
    public void setAge(int age) {
        this.age = age;
    }

    /**
     * @return the gender
     */
    public String getGender() {
        return gender;
    }

    /**
     * @param gender the gender to set
     */
    public void setGender(String gender) {
        this.gender = gender;
    }

    /**
     * @return the emailaddress
     */
    public String getEmailaddress() {
        return emailaddress;
    }

    /**
     * @param emailaddress the emailaddress to set
     */
    public void setEmailaddress(String emailaddress) {
        this.emailaddress = emailaddress;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the phonenumber
     */
    public String getPhonenumber() {
        return phonenumber;
    }

    /**
     * @param phonenumber the phonenumber to set
     */
    public void setPhonenumber(String phonenumber) {
        this.phonenumber = phonenumber;
    }

    /**
     * @return the yearofregistration
     */
    public int getYearofregistration() {
        return yearofregistration;
    }

    /**
     * @param yearofregistration the yearofregistration to set
     */
    public void setYearofregistration(int yearofregistration) {
        this.yearofregistration = yearofregistration;
    }

    /**
     * @return the qualificationtypes
     */
    public String getQualificationtypes() {
        return qualificationtypes;
    }

    /**
     * @param qualificationtypes the qualificationtypes to set
     */
    public void setQualificationtypes(String qualificationtypes) {
        this.qualificationtypes = qualificationtypes;
    }

    /**
     * @return the institution
     */
    public String getInstitution() {
        return institution;
    }

    /**
     * @param institution the institution to set
     */
    public void setInstitution(String institution) {
        this.institution = institution;
    }

    /**
     * @return the yearofcompletion
     */
    public int getYearofcompletion() {
        return yearofcompletion;
    }

    /**
     * @param yearofcompletion the yearofcompletion to set
     */
    public void setYearofcompletion(int yearofcompletion) {
        this.yearofcompletion = yearofcompletion;
    }

    
    
}
