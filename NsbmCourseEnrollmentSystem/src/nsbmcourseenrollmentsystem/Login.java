    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed

        if(txtusername.getText()==""){
            JOptionPane.showMessageDialog(null, "Please Enter User Name");
        }else{

            try{    // hard coded right usename & password feilds if enter wright things go forword
                String username=txtusername.getText();
                char[] pwd = pwdpassword.getPassword();
                String password = new String(pwd);
                    if((username.equals("nsbm"))&&(password.equals("green"))){
                        JOptionPane.showMessageDialog(null, "Vallid User");
                        Openingpage o = new Openingpage();
                        o.setVisible(true);
                        o.setLocationRelativeTo(null);                     //set to center
                       // o.setExtendedState(JFrame.MAXIMIZED_BOTH);      //set to full screen
                        o.pack();
                        this.dispose();
                    }else{
                        JOptionPane.showMessageDialog(null, "Invallid User and Try Again");
                        clearFeilds();
                    }
            }catch(Exception e){
                System.out.println(e.getMessage());
            }
        }
        
    }//GEN-LAST:event_btnloginActionPerformed

